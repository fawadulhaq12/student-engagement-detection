"""## ✅ STEP 5 — Build MobileNetV2 Model"""

# ── Load MobileNetV2 pre-trained on ImageNet ─────────────────────────────
base = MobileNetV2(
    weights      = 'imagenet',          # use ImageNet knowledge
    include_top  = False,               # remove original classifier
    input_shape  = (IMG_SIZE, IMG_SIZE, 3)
)

# Freeze first 100 layers (basic edge/colour detectors — already good)
# Train layers 100+ so the model learns face-expression features
for layer in base.layers[:100]:
    layer.trainable = False
for layer in base.layers[100:]:
    layer.trainable = True

frozen    = sum(1 for l in base.layers if not l.trainable)
trainable = sum(1 for l in base.layers if l.trainable)
print(f'MobileNetV2 — Frozen: {frozen}  Trainable: {trainable}')

# ── Build full model ──────────────────────────────────────────────────────
inputs = keras.Input(shape=(IMG_SIZE, IMG_SIZE, 3))

# preprocess_input converts 0-255 pixels to what MobileNetV2 expects (-1 to 1)
x = preprocess_input(inputs)

x = base(x, training=False)              # run through MobileNetV2
x = layers.GlobalAveragePooling2D()(x)   # flatten feature maps
x = layers.BatchNormalization()(x)       # stabilise training
x = layers.Dense(512, activation='relu')(x)
x = layers.Dropout(0.5)(x)              # 50% dropout to prevent overfitting
x = layers.Dense(256, activation='relu')(x)
x = layers.Dropout(0.3)(x)
outputs = layers.Dense(NUM_CLASSES, activation='softmax')(x)  # 6 class output

model = Model(inputs, outputs)

model.compile(
    optimizer = keras.optimizers.Adam(learning_rate=1e-4),
    loss      = 'categorical_crossentropy',
    metrics   = ['accuracy']
)

print(f'Total parameters : {model.count_params():,}')
print('✅ MobileNetV2 model ready!')

# ── Sanity check before training ─────────────────────────────────────────
# Predictions should be spread across different classes, not all the same
