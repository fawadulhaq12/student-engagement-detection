"""## ✅ STEP 3 — Split into Train / Val / Test (70 / 15 / 15)"""

# Source folders — exactly matching your zip structure
SOURCE_CLASSES = {
    'engaged':      f'{EXTRACT_PATH}/Student-engagement-dataset/Engaged/engaged',
    'confused':     f'{EXTRACT_PATH}/Student-engagement-dataset/Engaged/confused',
    'frustrated':   f'{EXTRACT_PATH}/Student-engagement-dataset/Engaged/frustrated',
    'bored':        f'{EXTRACT_PATH}/Student-engagement-dataset/Not engaged/bored',
    'drowsy':       f'{EXTRACT_PATH}/Student-engagement-dataset/Not engaged/drowsy',
    'Looking Away': f'{EXTRACT_PATH}/Student-engagement-dataset/Not engaged/Looking Away',
}

DATASET_PATH = '/content/dataset'
TRAIN_R, VAL_R = 0.70, 0.15       # 70% train · 15% val · 15% test
random.seed(42)                    # fixed seed → same split every run

print('📁 Splitting dataset…')
print('=' * 56)
total = {'train': 0, 'val': 0, 'test': 0}

for cls, src in SOURCE_CLASSES.items():
    # Verify the source folder exists
    if not os.path.exists(src):
        print(f'❌ MISSING: {src}')
        continue

    imgs = [f for f in os.listdir(src)
            if f.lower().endswith(('.jpg','.jpeg','.png'))]
    random.shuffle(imgs)

    n  = len(imgs)
    t1 = int(n * TRAIN_R)           # end of train slice
    t2 = int(n * (TRAIN_R + VAL_R)) # end of val slice

    splits = {'train': imgs[:t1], 'val': imgs[t1:t2], 'test': imgs[t2:]}

    for split, files in splits.items():
        dest = os.path.join(DATASET_PATH, split, cls)
        os.makedirs(dest, exist_ok=True)
        for f in files:
            shutil.copy2(os.path.join(src, f), os.path.join(dest, f))
        total[split] += len(files)

    tr, va, te = len(splits['train']), len(splits['val']), len(splits['test'])
    print(f'✅ {cls:15s} Train:{tr:4d}  Val:{va:4d}  Test:{te:4d}')

print('=' * 56)
print(f'   {"TOTAL":15s} Train:{total["train"]:4d}  Val:{total["val"]:4d}  Test:{total["test"]:4d}')
print('\n✅ Split complete!')

"""## ✅ STEP 4 — Load & Augment Images"""

IMG_SIZE   = 224    # MobileNetV2 native size
BATCH_SIZE = 32
NUM_CLASSES = 6

# ─── Training generator — WITH augmentation ───────────────────────────────
# NO rescale here! preprocess_input() inside the model handles normalisation.
