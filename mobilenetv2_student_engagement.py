print('🔍 Sanity check — 6 predictions before training:')
print(f'{"True Class":15s}  {"Pred Class":15s}  Confidence')
print('-' * 48)
for lbl, pred in zip(sample_lbls[:6], preds):
    true_cls = CLASS_LABELS[np.argmax(lbl)]
    pred_cls = CLASS_LABELS[np.argmax(pred)]
    conf     = np.max(pred) * 100
    print(f'{true_cls:15s}  {pred_cls:15s}  {conf:.1f}%')

print('\n✅ If Pred Classes above are varied → model is fine, proceed to training')
