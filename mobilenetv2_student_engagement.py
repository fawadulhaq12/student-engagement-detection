"""## ✅ STEP 6 — Train the Model"""

callbacks = [
    # Save best model automatically
    ModelCheckpoint(
        '/content/best_model.keras',
        monitor        = 'val_accuracy',
        save_best_only = True,
        mode           = 'max',
        verbose        = 1
    ),
    # Stop training if val_accuracy stops improving for 12 epochs
    EarlyStopping(
        monitor             = 'val_accuracy',
        patience            = 12,
        restore_best_weights= True,
        verbose             = 1
    ),
    # Halve learning rate if val_loss stalls for 4 epochs
    ReduceLROnPlateau(
        monitor  = 'val_loss',
        factor   = 0.5,
        patience = 4,
        min_lr   = 1e-7,
        verbose  = 1
    )
]

print('🚀 Training MobileNetV2…')
print('Expected: accuracy > 50% by epoch 5, final ~75-85%')
print('=' * 60)

history = model.fit(
    train_data,
    epochs          = 100,
    validation_data = val_data,
    callbacks       = callbacks,
    verbose         = 1
)

best_val = max(history.history['val_accuracy'])
